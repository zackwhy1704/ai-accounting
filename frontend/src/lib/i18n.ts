export type TranslationKey = keyof typeof translations.en

export function getTranslation(lang: string, key: TranslationKey, params?: Record<string, string>): string {
  const table = translations[lang as keyof typeof translations] ?? translations.en
  let text = (table as Record<string, string>)[key] ?? (translations.en as Record<string, string>)[key] ?? key
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(`{${k}}`, v)
    }
  }
  return text
}
